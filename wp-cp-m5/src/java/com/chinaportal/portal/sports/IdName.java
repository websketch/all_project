package com.chinaportal.portal.sports;

public class IdName {
	
	private String id;
	private String name;
	private boolean isTeam;
	
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}

	public boolean isTeam() {
		return isTeam;
	}
	

	public void setTeam(boolean isTeam) {
		this.isTeam = isTeam;
	}
	
	

}
