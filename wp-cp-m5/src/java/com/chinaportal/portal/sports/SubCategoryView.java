package com.chinaportal.portal.sports;

public class SubCategoryView {
	private String id;
	private String name;
	private java.util.List matchs;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public java.util.List getMatchs() {
		return matchs;
	}
	public void setMatchs(java.util.List matchs) {
		this.matchs = matchs;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
